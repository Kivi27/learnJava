import java.util.Scanner;

class plusTwoNum {
    public static void main(String[] args)
    {
        Scanner scanner = new Scanner(System.in);
        int a = scanner.nextInt();
        int b = scanner.nextInt();
        scanner.close();
        System.out.println("result " + a + " + " + b + "  = " + (a+b));
    }
}
